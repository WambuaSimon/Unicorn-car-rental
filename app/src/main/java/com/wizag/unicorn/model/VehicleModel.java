package com.wizag.unicorn.model;

public class VehicleModel {
    String carMake;
    String carImage;
    String carModel;
    String fuelType;
    String transmission;
    String bodyDesign;
    String dailyPricing;
    String weeklyPricing;
    String monthlyPricing;

    public String getCarMake() {
        return carMake;
    }

    public void setCarMake(String carMake) {
        this.carMake = carMake;
    }

    public String getCarImage() {
        return carImage;
    }

    public void setCarImage(String carImage) {
        this.carImage = carImage;
    }

    public String getCarModel() {
        return carModel;
    }

    public void setCarModel(String carModel) {
        this.carModel = carModel;
    }

    public String getFuelType() {
        return fuelType;
    }

    public void setFuelType(String fuelType) {
        this.fuelType = fuelType;
    }

    public String getTransmission() {
        return transmission;
    }

    public void setTransmission(String transmission) {
        this.transmission = transmission;
    }

    public String getBodyDesign() {
        return bodyDesign;
    }

    public void setBodyDesign(String bodyDesign) {
        this.bodyDesign = bodyDesign;
    }

    public String getDailyPricing() {
        return dailyPricing;
    }

    public void setDailyPricing(String dailyPricing) {
        this.dailyPricing = dailyPricing;
    }

    public String getWeeklyPricing() {
        return weeklyPricing;
    }

    public void setWeeklyPricing(String weeklyPricing) {
        this.weeklyPricing = weeklyPricing;
    }

    public String getMonthlyPricing() {
        return monthlyPricing;
    }

    public void setMonthlyPricing(String monthlyPricing) {
        this.monthlyPricing = monthlyPricing;
    }
}
